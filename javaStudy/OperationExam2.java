public class OperationExam2 {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int c = 15;
        System.out.println(a - b * c);
        System.out.println(a > b && b > 5);
        System.out.println(a > b || b > 5);
        System.out.println(++a - 5);
    }
}
